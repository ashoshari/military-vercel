interface TreeNode {
  id: string;
  label: string;
  labelEn?: string;
  value: number;
  children?: TreeNode[];
}
export function secondarySalesMetric(node: TreeNode): number {
  const t = node.id.split("").reduce((a, c) => a + c.charCodeAt(0), 0);
  return Math.max(0, Math.round(node.value * (0.19 + (t % 17) * 0.004)));
}
